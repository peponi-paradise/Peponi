namespace Peponi.Math.UnitConversion;

public enum AngularSpeedUnit
{
    /// <summary>
    /// <code>
    /// rad / s
    /// </code>
    /// </summary>
    RadianPerSecond,

    /// <summary>
    /// <code>
    /// rad / min
    /// </code>
    /// </summary>
    RadianPerMinute,

    /// <summary>
    /// <code>
    /// rad / h
    /// </code>
    /// </summary>
    RadianPerHour,

    /// <summary>
    /// <code>
    /// rad / d
    /// </code>
    /// </summary>
    RadianPerDay,

    /// <summary>
    /// <code>
    /// cyc / s
    /// </code>
    /// </summary>
    CyclePerSecond,

    /// <summary>
    /// <code>
    /// cyc / min
    /// </code>
    /// </summary>
    CyclePerMinute,

    /// <summary>
    /// <code>
    /// cyc / h
    /// </code>
    /// </summary>
    CyclePerHour,

    /// <summary>
    /// <code>
    /// cyc / d
    /// </code>
    /// </summary>
    CyclePerDay,

    /// <summary>
    /// <code>
    /// ° / s
    /// </code>
    /// </summary>
    DegreePerSecond,

    /// <summary>
    /// <code>
    /// ° / min
    /// </code>
    /// </summary>
    DegreePerMinute,

    /// <summary>
    /// <code>
    /// ° / h
    /// </code>
    /// </summary>
    DegreePerHour,

    /// <summary>
    /// <code>
    /// ° / d
    /// </code>
    /// </summary>
    DegreePerDay,

    /// <summary>
    /// <code>
    /// r / s
    /// rev / s
    /// </code>
    /// </summary>
    RevolutionPerSecond,

    /// <summary>
    /// <code>
    /// r / min
    /// rev / min
    /// </code>
    /// </summary>
    RevolutionPerMinute,

    /// <summary>
    /// <code>
    /// r / h
    /// rev / h
    /// </code>
    /// </summary>
    RevolutionPerHour,

    /// <summary>
    /// <code>
    /// r / d
    /// rev / d
    /// </code>
    /// </summary>
    RevolutionPerDay,
}